# celsius - fahrenheit change

celsius = float(input("Enter temperature in celsius: ")) 
fahrenheit = (celsius * 1.8) + 32 
print(str(celsius) + " degree Celsius is equal to " + str(fahrenheit) + " degree Fahrenheit.")

varenheit = float(input("Enter temperature in Farenheit: ")) 
zelsius = (varenheit - 32) / 1.8
print(str(varenheit) + " degrees Fahrenheit is equal to " + str(zelsius) + " degrees Celsius")

